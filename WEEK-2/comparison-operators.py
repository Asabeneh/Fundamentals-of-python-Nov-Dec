# what are the comparsion operators?
# >, >=, <, <=, ==, !=
# whenever we compare, the value will be a boolean data type(True or False)

value = 3 > 2

print(value)
print(3 >= 2)
print(3 < 2)
print(3 <= 2)
print(3 == 3)
print(3 == '3')
print(3 == int('3'))
print(3 != 3)
print(3 is not 4)
print(not not value)